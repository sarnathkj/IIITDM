		<h3 class="text-center">Placement Procedure</h3>
		<ol class="text-justify">
			<li>Placement Cell (nodal point for placements at IIITDM Kancheepuram), assisted by
				  student representatives, sends invitation to companies along with relevant
					information and documents - Placement Brochure, Employer Registration Form (ERF), etc.</li>

<br/>

			<li>A company can also show interest in recruiting IIITDM Kancheepuram students by
				contacting Placement Cell, IIITDM Kancheepuram by writing to placement@iiitdm.ac.in, stating
				intent to visit IIITDM Kancheepuram campus for recruitment. ERF can also be downloaded from our
				Training &amp; Placement website.</li>

<br/>

			<li>After receiving filled in ERF and relevant information from a company T&amp;P Cell
				will reply within 2-3 working days through email. The dates for campus interviews are allotted
				on the basis of information provided in ERF. It is expected that correct and complete information
			  is provided by companies in ERF.</li>

				<br/>

			<li>Suitable date for the Pre-Placement Talk (PPT) is decided by the discussions between the
				companies and the T&amp;P Cell. After confirmation from the companies, students are notified of
				the PPT date.  However, PPT and recruitment process can also go together.</li>

				<br/>

			<li>Interested students will register for a particular company online through an internal website
				or by hard copies.  After the dead line, the information will be forwarded to companies.</li>

				<br/>

			<li>Companies are required to send the list of short listed students by email to the T&amp;P Cell
				prior to the campus visit for final interviews.</li>

				<br/>

			<li>The companies visit the campus for placements on the allotted dates and conduct Group Discussions/Aptitude
				  test/Technical test/Personal Interviews etc., as part of their preferred selection procedure.</li>

					<br/>

			<li>The companies are expected to furnish the final list of selected students on the same day after the
				selection procedure is completed.  This will enable the Institute by not allowing the selected students
				to the companies visiting at later dates.</li>

				<br/>

			<li>The ERF provides the primary basis of communicating the details of the positions offered by the company
				  to the candidates. It is therefore, highly desirable that the form is completed in all respects.</li>

					<br/>

			<li>The decision regarding the dates is on the discretion of the T&amp;P cell. Companies are expected to
				  strictly adhere to the time and dates slots allowed to them. Any change must be notified in advance.</li>

					<br/>

			<li>Please note that the facility of video conferencing is available at the campus and can be availed for
				  the purpose of interviewing candidates in case the company is unable to visit the campus.</li>

					<br/>

		</ol>
