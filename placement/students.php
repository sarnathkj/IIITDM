		<div class="col-sm-8 col-md-8 col-xs-12 col-lg-8 col-sm-offset-2 col-xs-offset-0" id="row3">
		<h3 class="text-center">Eligiblity criteria for placement registration</h3>
		<ol class="text-justify">
			<li>All students who expect to graduate from the institute by the end of the
					academic year 2015- 2016 and are seeking employment.</li>

<br/>

			<li>Students having 3 or more course backlogs are advised not to register for placement.</li>

<br/>

			<li>Students who have a pre-placement offer will also be allowed to register
				for the institute placement process.</li>

				<br/>

			<li>Each student should register online by uploading the filled resume in the prescribed
			format along with the registration fees and submit the hardcopy of the student placement
			registration form to the placement office.</li>

				<br/>

			<li>All students who wish to take part in the institute campus placements should register
				for soft skills programme organized by the institute.</li>

				<br/>

			<li>Companies are required to send the list of short listed students by email to the T&amp;P Cell
				prior to the campus visit for final interviews.</li>

				<br/>

			<li> All Students should compulsorily attend all the soft skills classes failing which will lead
				to cancellation of placement registration.</li>

					<br/>

			<li>Students who have lost the job for genuine reasons will be allowed to re-register on a case to case basis.
				Only students who have a course extension or other genuine reasons will be allowed to re-register.</li>

				<br/>

		</ol>
		<h3 class="text-center">Resume - Student Placement Registration</h3>
		<ol class="text-justify">

			<li>nformation regarding company visits will be informed through e mail/ notice board/ website. Students are advised
			to check timely announcements, go through the company website and apply only if interested.</li>

			<br/>

			<li>Students are expected to follow the institute resume template available in the placement website for
		  preparing the resumes.</li>

			<br/>

			<li>The details of the resume have to be genuine and any student found violating this, will not be permitted
			to apply for placements for the rest of the academic year.</li>

			<br/>

			<li>Depending on the profile/requirements laid by the respective company a detailed resume should be prepared
			in an attractive manner and submitted within the deadline. Late submission will not be considered for the
			test/interview.
      </li>

			<br/>

		</ol>
		<h3 class="text-center">Pre-Placement Interaction (PPI)</h3>
		<ol class="text-justify">

			<li>Those who have submitted the resume and attended the PPI session are eligible to appear in the
				selection process of that company. The PPI attendance is compulsory.</li>

				<br/>

				<li>Students must clarify their queries regarding salary break-up, job profile, place of work, bond details,
				medical fitness requirements, etc with the company officials during PPI.</li>

				<br/>

				<li> There shall no question about the pay package offered by the company. Students shall not
				question the policy of the company.</li>

				<br/>

				<li>The interaction with the representatives of the companies should be in a very dignified manner.</li>

				<br/>

		</ol>
		<h3 class="text-center">Placement Process</h3>
		<ol class="text-justify">

			<li>It is the responsibility of the student to check announcements/notices/updated information/ short listed
				names etc., in the notice boards/email/website. Students are expected to report at the respective venue as
				per the announcements.</li>

				<br/>

			<li>Late comers for the Aptitude Test / Group Discussion / Interview may not be allowed to appear
				for the selection process.</li>

				<br/>

		</ol>
		<h3 class="text-center">Attendance and Punctuality</h3>
		<ol class="text-justify">

			<li>A student who applies and gets short listed is bound to go through the entire selection process
				unless rejected midway by the recruiter.</li>

				<br/>

				<li>Any student who withdraws deliberately in the midst of a selection process will be disqualified
				from placement for the rest of the academic year.</li>

				<br/>

				<li>Unauthorized absence for the test/interview will lead to cancellation of registration.</li>

				<br/>

		</ol>
		<h3 class="text-center">Dress code</h3>
		<p class="text-justify"> Students must be formally dressed whenever they participate in any sort of interaction
		 with a company. The coordinator deserves the right to refuse permission to a student to attend the selection
		 process, if their attire is unsatisfactory .The dress code shall be applicable for Pre placement talks as well.
     <br/><br/>Boys - Formal dark pant with light colored full sleeve shirt with blazers, neck tie and formal shoes
     <br/><br/>Girls - Churidar / Salwar / Shirt-Pant with overcoat and shoes</p>

		 <br/>
		 <h3 class="text-center">Identity card</h3>
		 <p class="text-justify">Students must bring their identity cards whenever they undergo a placement process.</p>

		 <br/>
		 <h3 class="text-center">Job Offer</h3>
		 <ol class="text-justify">
			 <li>Announcement on the notice board will be considered as the final offer. Offers received from companies
				must be collected as per timings in circular /notice.</li>

				<br/>

				<li>The students shall not question the company on why he/she is not selected. Selection is in the hands of
					the company. Any unruly behaviour compromising the reputation of the institute shall deem the student
					ineligible for future placements and the student shall face the disciplinary committee.</li>

					<br/>

					<li> The responsibility of going through the offer letter and taking actions therein such as submission of
					documents lies entirely with the student.</li>

					<br/>

					<li>The students should honour the offer and communicate with the Industry in a pleasing manner.</li>

					<br/>

					<li>In case offers are received directly by the student from the company, the same must be intimated
						to the placement office.</li>

					<br/>

		 </ol>
		 <h3 class="text-center">Multiple offers</h3>
		 <p class="text-justify">A student is eligible for only one job. If a student receives more than one offer owing
		to delays in the announcements of results by the recruiters, he/she is bound to accept the job whose results are
		declared first. If the results are declared on the same day, the student may choose from the offers in hand and
		inform the placement coordinator within 2 days of the announcement of the results.</p>

		<br/>

		<h3 class="text-center">Joining status</h3>
		<p class="text-justify">Students should notify the company with a copy to the placement office in case they are
		not joining the company with reasons. For all matters not covered by the above regulations, the placement
		coordinators will use their discretion to take appropriate decisions.</p>

