	with a personal Super Computer employing 4 Tesla cards giving a peak performance of 4.12 Tflops to assist in the
	advanced courses on Computer Architecture, Advanced Structure/Algorithms, Database Data Management/Knowledge
	Engineering and Image Processing. The HCI laboratory houses a powerful image processing workstation with intel x5690 6
	core processor with dual monitor support.
