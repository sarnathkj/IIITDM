                    <p>The Master of Design program is a four-semester (two-year) full-time post graduate program leading to the M.Des (Electronic Systems / Mechanical Systems) degree of the Institute. The program aims at developing a breed of product designers with a sound knowledge of the various phases of a product development process, to become team leaders of a Product Development team. With India poised to become a globally preferred destination for New Product Development (NPD), the program prepares the students for lucrative global careers. This program imbibes in the students, the various skills and the knowledge required to usher in innovation in the electronic or mechanical industry. It challenges the students to look at engineering design through creative problem solving abilities.</p>
                    
                    <p>From the early stages of Conceptual Development, through planning for manufacture to retirement of the products, the course enables students to acquire knowledge in all aspects of Product Life-cycle Management (PLM) and incorporate them in the early design phase. A thorough exposure to the effective use of tools for product design and Design Studio is provided through a combination of appropriate theory and practice. The program is on par with any of the existing M.Tech degree programs in Product Design offered by premier institutions in the country, with considerable scope for a bright career in industry or for a research career in relevant areas. Realizing the importance of design in the economic development of the nation and in making our industries globally competitive, the Government of India has developed a National Design Policy aiming at raising the standards of design education to global standards of excellence and creating the right opportunities for our industries to offer new and innovative products and services. These initiatives provide the students who go through this program unlimited international career opportunities.</p>
                    
                    <p>IIITDM Kancheepuram functions with the mandate of excelling in design & manufacturing education and research. With a well established novel UG curricula in place, the Institute introduced innovative 2 year postgraduate programmes in 2010. M.Des (Master of Design) is currently offered in Communication Systems Design, Mechanical Systems Design and Electronic Systems Design. M.Des programmes of IIITDM are aimed to specialize Mechanical and Electrical/Electronics Engineers with design skills required for the design and development of mechanical and electronic products.</p>
                    
                    <p>The curricula of the post graduate engineering design programs specializing in Mechanical and Electronic Systems created by experts from various design centers, academic institutes such as IITs, IISc, etc. and renowned industries. The curricula offers a blend of design thinking and core engineering subjects required for innovative product design. M.Des programs promote efficient, effective, and desirable human-computer interaction; visualization and navigation through information spaces; time-based information design; and collaborative design practice among different disciplines.</p>
                    
                    <p>M.Des engineers passing out from IIITDM Kancheepuram will find scope and relevance in analog and digital electronics IC companies, embedded systems players, automotive design, consumer product design and manufacturing industries. The curricula of the proposed M.Des in Communication systems programme will be in line with the ‘Learning By Doing’ motto of the institute and would have dedicated practice sessions as part of the core courses. The program aims to bridge the gap between communication and hardware engineers by imparting product / component design aspects for the communication industry and, hence produce next generation communication technocrats.</p>
                    
                    <p>Proposed M.Des curriculum has additional conceptual design courses focused on product design tools/issues. Product design theory and relevant lab courses supplement the core courses and incorporate the design flavor required in the curricula. Topics related to electromagnetic interference and compatibility and RF circuits are adequately highlighted through theory and practice courses. Further issues/concepts related to digital signal and random processes are also effectively covered in the curriculum.</p>
                    
                    <p>M.Des in Communication Systems was offered from the academic session from August 2012. The incessant demand for increased transmission bandwidth has led to the rapid technological solutions/changes in the modern communication systems in recent times. The sector is exploding with job opportunities and is considered as one of the fastest growing/expanding technological area. Our country, having made rapid inroads in the software and information technology sector, is short of quality manpower equipped with necessary tools and technological knowledge to meet the requirements of the electronic hardware sector. The proposed M.Des in Communication Systems curriculum of IIITDM Kancheepuram adopts a balanced approach to the industry requirements with courses on digital communication and communication networks, PCB and embedded system design are included with sufficient coverage of VLSI/FPGA design and VERILOG implementation. With mastery in domain specific design, engineering skills and required managerial expertise, our graduates can entrepreneur organizations involved in the design and manufacture of commercially successful electronics or mechanical products.</p>
                    
