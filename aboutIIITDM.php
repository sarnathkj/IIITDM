            <h4>About IIITDM Kancheepuram</h4>
            <p>Globalization has created uniformity in customer expectations world over. With opening up of Indian economy, our manufacturing sector has to compete globally even for the domestic market. This would require strong products with leading technology / quality and compelling cost advantage. Suitably trained manpower with skill sets appropriate for design, development and prototyping using modern IT tools is critical to achieve this goal. This Institute is envisioned as an academic institution of excellence that would facilitate and promote the competitive advantage of Indian products in global markets. This Institute will serve as an inter-disciplinary institution for education and research in the area of Product Lifecycle Management (PLM) encompassing design and manufacturing using state of the art concepts, tools, processes and practices of the industry world over.</p>
            <p>Indian Institute of Information Technology Design and Manufacturing Kancheepuram (IIITDM Kancheepuram) is a Centre of Excellence for technical education and research established in 2007 by the Ministry of Human Resource Development, Government of India to pursue design and manufacturing oriented engineering education and research and to promote the competitive advantage of Indian products in global markets.</p><br />
            <p>It is one of the two IIITDMs, the other one being located in Jabalpur. The institute,which was operating from its temporary campus within the Indian Institute of Technology Madras, which was also its mentor institute, has moved to its permanent campus at Kelambakkam-Vandalur road near Chennai in 2011. The institute offers academic and research programs that integrate engineering design, manufacturing and management with information technology. The institute offers undergraduate and postgraduate programs which focus on IT, design and manufacturing in engineering sectors.</p>
            <p>The institute is located on a 51 acre campus on the outskirts of Chennai, off the Vandalur-Kelambakkam road. It is a fully residential campus with all students residing within the campus. The campus houses the academic block, the administrative block, lecture hall complex and laboratory block.</p>
            <p>The Institute is presently offering B.Tech programmes in Computer Engineering, Electronics and Communication Engineering (Design & Manufacturing) and Mechanical Engineering (Design & Manufacturing), 5 Dual Degree Programmes leading to B.Tech and M.Tech Degrees, M.Des in Electronic Systems Design, Communication Systems Design, Mechanical Systems Design and inter-disciplinary Ph.D programmes in core and applied areas of engineering. From the academic year 2016, the Institute will introduce the B.Tech Mechanical (Smart Manufacturing). The faculty members of the Institute are also involved in sponsored and consultancy research projects from reputed industries and government organizations.</p>
            <p>The curricula for all the programs have been framed after extensive deliberations and discussions with IITs, IISc and other reputed Institute faculty members and industry stake holders. The programmes are aimed at providing solutions to current technological challenges in wide domains like robotics, home entertainment, telecommunication automotive systems, etc. Design engineers and researchers passing out from IIITDM Kancheepuram will find scope and relevance in IT and IT enabled product companies, in particular analog and digital electronics IC companies, embedded systems players, automotive design, consumer product design and manufacturing and knowledge/service based industries. With mastery in domain specific design, engineering skills and required managerial expertise, our graduates can entrepreneur organizations involved in the design and manufacture of commercially successful electronic or mechanical products</p>

