            <p>To become a premier institution of excellence in Design and Manufacturing that would create and develop a new generation of engineers and technologists with the ability and mindset to lead Indian industries in globally competitive economic environment.</p>
            
            <h4 id="mission">Mission</h4>
            <p>To be a world class apex centre of excellence in education, research,development and training in Design and Manufacturing.</p>
            
