                <p>Welcome to IIITD&M Kanchepuram . Bringing change by Design is reflected in all aspects of IIITDM – teaching, experiential learning, research, product development, student activities, infrastructure development, campus administration. To become a premier institute of excellence in design and manufacturing, the institute presently offers novel and first of their kind under-graduate programmes leading to B.Tech in Computer Engineering, Electronics Engineering, and Mechanical Engineering. With our humble beginning with just three B.Tech programmes with an intake of 20 each, we gradually increased our intake and I am glad to highlight that from the next academic year onwards we will be offering Dual degree programmes in all three streams with an increased intake of 80 students in each stream.</p>
                <p>The Institute is progressing at a rapid pace and has witnessed an excellent growth in all spheres, thanks to aggressive and enthusiastic faculty, staff, and students. The institute has established itself as one of the centres of excellence in Design and Manufacturing ever since its inception in 2007. Over the past year IIITDM has evolved as a design centric institute in Information Technology and Information technlogy enabled design and manufacturing.</p>
                <p>Design centric coupled with experiential learning approach has attracted many product development companies to our campus for campus hiring and internships. Many under graduate students are pursuing Graduate and Research programmes in institues of international repute like Stanford University, Georgia Tech, Columbia University, University of California, IITM, etc., Our vision is reinforced by the fact that our students have developed products such as ‘Smart Book Holder for Differently Abled’, ‘Reading Tool for Visually Challenged’. On the IT front, ‘faculty recuritment system’, ‘Ph.D Application Scruitny’ system were developed by enthusiastic under graduate students.</p>
                <p>On the academic front, with successful B.Tech programmes in store, IIITDM has taken a radical path in introducing novel Post Graduate programmes leading to M.Des and on offer there are three programmes, two in Electronics stream and the other in Mechanical Engineering Stream. The emphasis of PG programmes is either on Design or on Manufacturing to facilitate deep study in design thinking or in product development. Some of our PG students have been absorbed by Design/Product companies and the rest are pursuing research in IITs/IIITs.</p>
                <p>IIITDM maintains highest academic quality for both teaching and research. Our novel teaching pedagogy-learning by doing facilitates good understanding of rudimentary concepts. This has helped our third year students to get internships in leading industries and research Academia. Notable ones are, Internships at Max-Planck Institute for Informatics, Germany under DAAD, Hitachi-NUT, Japan and IIT Madras.</p>
                <p>On the research front, our vibrant faculty group with Ph.D scholars have contributed 42 papers in peer-reviewed journals and conferences in the last academic year. Also, there are 6 sponsored projects and various academic research projects are on the list. Moreover, a lot of emphasis is on research in all courses taught at IIITDM. Being a premier institute, our vibrant faculty enthusiastically organizes workshop, conferences, and research internships. IConDM’13 – The first international conference in Design and Manufacturing held at IIITDM attracted 519 research articles globally of which 173 were accepted for presentation.MOUs between IIITDM and Nagaoka University, Japan , University of Catania, Italy, University of Genova, Italy, have been signed to faciliate student exchange programmes and research collaboration.</p>
                <p>As regards to extra-curriculur activites, our students have received many awards in Inter Sports Meet at IIIT Gwalior and over all, IIITDM secured the third place. Institute cultural and technical festival SAMGATHA’14 have attracted a good lot of participants across the country. Samgatha is a place for great minds to meet and exhibit their talents. Our Social service group is very active throughout the academic year with activities such as free eye-checkup to local citizens, free computer education to school children, Blood donation camp, Polio drops drive, and tree planatation.</p>
                <p>As far infrastructure development is concerned, institute has made a giant leap with a sizeable increase in the project works completed / undertaken. Institute Administration started functioning from new building from this semester, and the construction of Lecturer Hall Complex, Laboratory complex, Multi-storey Hostel are in full swing and expected to become operational from the next semester.</p><br>                
